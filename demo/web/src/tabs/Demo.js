// IDRiD localization pool for the Random button. Unlike EyePACS, IDRiD ships
// pixel-accurate GROUND-TRUTH optic-disc + fovea centres (Localization
// sub-challenge), sourced from `_idridSamples.js`
// (regenerated by `demo/web/scripts/prepare_idrid_samples.py`).
