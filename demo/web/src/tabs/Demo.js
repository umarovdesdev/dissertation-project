let lastIdridIdx = -1;
function pickRandomIdridPatient() {
  if (!IDRID_PATIENTS || IDRID_PATIENTS.length === 0) return null;
  let idx;
  do {
    idx = Math.floor(Math.random() * IDRID_PATIENTS.length);
  } while (IDRID_PATIENTS.length > 1 && idx === lastIdridIdx);
  lastIdridIdx = idx;
  return IDRID_PATIENTS[idx];
}

